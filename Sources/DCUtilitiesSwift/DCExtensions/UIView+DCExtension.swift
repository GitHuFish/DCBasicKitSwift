import UIKit

// MARK: - UIView 扩展
public extension UIView {    
    /// Safe Area
    var dc_safeAreaInsets: UIEdgeInsets {
        if #available(iOS 11.0, *) {
            return self.safeAreaInsets
        } else {
            return .zero
        }
    }
}
